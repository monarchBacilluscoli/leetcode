// @lc code=start
class Solution
{
public:
    std::vector<std::vector<int>> subsets(std::vector<int> &nums)
    {
        std::vector<std::vector<int>> result;
        //????????????????????????????????1.????????????????????????? 2.????????????????????????
        result.push_back({});
        for (size_t i = 0; i < nums.size(); ++i)
        {
            int sz = result.size();
            for (int j = 0; j < sz; ++j)
            {
                std::vector<int> r = result[j];
                r.push_back(nums[i]);
                result.push_back(r);
            }
        }
        return result;
    }
};
